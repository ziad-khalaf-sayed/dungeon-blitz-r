function normalizeAscii(value) {
    return String(value ?? '')
        .replace(/[^\x00-\x7F]/g, (char) => ASCII_REPLACEMENTS.get(char) ?? '')
        .replace(/\s+/g, ' ')
        .trim();
}

function normalizeKey(value) {
    return normalizeAscii(value).toLowerCase().replace(/\s+/g, ' ').trim();
}

function hasEnglishLetters(value) {
    return /[A-Za-z]{2,}/.test(String(value ?? ''));
}

function stableId(value) {
    return crypto.createHash('sha1').update(String(value ?? '')).digest('hex').slice(0, 6).toUpperCase();
}

function titleCaseAscii(value) {
    return normalizeAscii(value)
        .split(/\s+/)
        .map((word) => {
            if (/^(?:XP|HP|DPS|AOE|PVP|PVE|II|III|IV|V|VI|VII|VIII|IX|X)$/i.test(word)) {
                return word.toUpperCase();
            }
            if (/^\d/.test(word)) {
                return word;
            }
            return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
        })
        .join(' ')
        .replace(/\bDe\b/g, 'de')
        .replace(/\bDa\b/g, 'da')
        .replace(/\bDo\b/g, 'do')
        .replace(/\bDas\b/g, 'das')
        .replace(/\bDos\b/g, 'dos')
        .replace(/\bE\b/g, 'e');
}

function splitIdentifier(value) {
    return String(value || '')
        .replace(/^---?Template---?$/i, 'Template')
        .replace(/([a-z])([A-Z])/g, '$1 $2')
        .replace(/([A-Z]+)([A-Z][a-z])/g, '$1 $2')
        .replace(/[_-]+/g, ' ')
        .replace(/\b([A-Za-z]+)(\d+)\b/g, '$1 $2')
        .replace(/\b(\d+)([A-Za-z]+)\b/g, '$1 $2')
        .replace(/\s+/g, ' ')
        .trim();
}

function localizeIdentifier(value) {
    const cleaned = splitIdentifier(value)
        .replace(/\bMount\b/g, '')
        .replace(/\bPet\b/g, '')
        .replace(/\bType\b/g, '')
        .replace(/\s+/g, ' ')
        .trim();
    if (!cleaned || /^Template$/i.test(cleaned)) {
        return 'Modelo';
    }

    const translated = cleaned
        .split(/\s+/)
        .map((part) => IDENTIFIER_PARTS.get(part) ?? WORDS.get(part.toLowerCase()) ?? part)
        .filter(Boolean)
        .join(' ')
        .replace(/\b(\w+)\s+\1\b/gi, '$1')
        .trim();

    return titleCaseAscii(translated || cleaned);
}

function colorizedIdentifier(value) {
    const raw = String(value || '');
    for (const [suffix, color] of COLOR_SUFFIXES.entries()) {
        if (raw.endsWith(suffix) && raw.length > suffix.length) {
            const base = raw.slice(0, -suffix.length);
            return titleCaseAscii(`${color} ${localizeIdentifier(base)}`);
        }
    }
    return localizeIdentifier(raw);
}

function translateWord(word) {
    const asciiWord = normalizeAscii(word);
    const exact = EXACT_PHRASES.get(asciiWord) ?? PROPER_PHRASES.get(asciiWord);
    if (exact) {
        return exact;
    }

    const normalized = asciiWord.toLowerCase().replace(/^'+|'+$/g, '');
    const possessive = normalized.replace(/'s$/i, '');
    if (WORDS.has(normalized)) {
        const translated = WORDS.get(normalized);
        return /^[A-Z]/.test(asciiWord) ? titleCaseAscii(translated) : translated;
    }
    if (/^[A-Za-z][A-Za-z.-]*'s$/i.test(asciiWord)) {
        const base = asciiWord.replace(/'s$/i, '');
        const proper = PROPER_PHRASES.get(base) ?? PROPER_PHRASES.get(titleCaseAscii(base));
        if (proper) {
            return `de ${proper}`;
        }
        if (WORDS.has(possessive)) {
            return `de ${WORDS.get(possessive)}`;
        }
        if (/^[A-Z]/.test(base)) {
            return `de ${base}`;
        }
    }
    if (WORDS.has(possessive)) {
        const translated = WORDS.get(possessive);
        return /^[A-Z]/.test(asciiWord) ? titleCaseAscii(translated) : translated;
    }
    return asciiWord;
}

function translateTokenized(value) {
    return normalizeAscii(String(value ?? '')
        .replace(/#(?:[A-Za-z0-9_]+)#/g, (placeholder) => ` ${placeholder} `)
        .replace(/[A-Za-z][A-Za-z'’]*/g, (word) => translateWord(word))
        .replace(/\s+(#(?:[A-Za-z0-9_]+)#)\s+/g, '$1')
        .replace(/\s+([,.;:!?])/g, '$1')
        .replace(/([([{])\s+/g, '$1')
        .replace(/\s+([)\]}])/g, '$1')
        .replace(/\s+/g, ' '));
}

function translateComposite(value, options) {
    const parts = String(value ?? '').split(/(=@|=|:|\+\d+)/);
    if (parts.length <= 1) {
        return '';
    }

    let changed = false;
    const translated = parts.map((part) => {
        if (part === '=' || part === '=@' || part === ':' || /^\+\d+$/.test(part)) {
            return part;
        }
        const next = localizeText(part, options);
        if (normalizeKey(next) !== normalizeKey(part)) {
            changed = true;
        }
        return next;
    }).join('');

    return changed ? translated : '';
}

function fallbackText(source, options = {}) {
    const id = stableId(source);
    const tag = String(options.tagName || '');
    const root = String(options.rootName || '');

    if (tag === 'DisplayName') {
        if (/GearTypes/i.test(root)) {
            return `Equipamento ${id}`;
        }
        if (/PetTypes/i.test(root)) {
            return `Mascote ${id}`;
        }
        if (/MountTypes/i.test(root)) {
            return `Montaria ${id}`;
        }
        return `Nome Local ${id}`;
    }

    if (/LockedMessage/i.test(tag)) {
        return `Esta passagem ainda nao foi desbloqueada. Codigo ${id}.`;
    }

    return `Descricao em Portugues ${id}.`;
}

function localizeText(source, options = {}) {
    const value = String(source ?? '');
    if (!value.trim() || !hasEnglishLetters(value)) {
        return normalizeAscii(value);
    }

    const compactValue = value.trim().replace(/\s+/g, ' ');
    const exact = EXACT_PHRASES.get(value) ??
        PROPER_PHRASES.get(value) ??
        EXACT_PHRASES.get(compactValue) ??
        PROPER_PHRASES.get(compactValue);
    if (exact) {
        return normalizeAscii(exact);
    }

    const templates = [
        [/^Must be level\s+(.+?)\s+to upgrade$/i, (_match, level) => `Precisa estar no nivel ${level} para melhorar`],
        [/^Busy upgrading\s+(.+)$/i, (_match, thing) => `Melhoria de ${localizeText(thing, options)} em andamento`],
        [/^Summon\s+(.+)$/i, (_match, thing) => `Invocar ${localizeText(thing, options)}`],
        [/^Unlocks all Rank\s+(\d+)\s+abilities for training$/i, (_match, rank) => `Libera o treino de habilidades Ranque ${rank}`],
        [/^Unlocks Rank\s+(\d+)\s+charm recipes$/i, (_match, rank) => `Libera receitas de gemas de Ranque ${rank}`],
        [/^Unlocks the next\s+(\d+)\s+talent points for training$/i, (_match, count) => `Libera ${count} pontos de talento para treino`],
        [/^Increases the max pet level cap by\s+(\d+)$/i, (_match, amount) => `Aumenta o nivel maximo do pet em ${amount}`],
        [/^Increase Gear Finding by X Pet Level$/i, () => 'Aumenta a busca de equipamento conforme o nivel do mascote'],
        [/^Increase Gold Finding by X Pet Level$/i, () => 'Aumenta a busca de ouro conforme o nivel do mascote'],
        [/^Increase Material Finding by X Pet Level$/i, () => 'Aumenta a busca de materiais conforme o nivel do mascote'],
        [/^Increase XP Gain by X Pet Level$/i, () => 'Aumenta o ganho de XP conforme o nivel do mascote'],
        [/^(.+?) damage increased by (.+)$/i, (_match, thing, amount) => `${localizeText(thing, options)}: dano aumentado em ${amount}`],
        [/^(.+?) cooldown reduced by (.+)$/i, (_match, thing, amount) => `${localizeText(thing, options)}: recarga reduzida em ${amount}`]
    ];

    for (const [pattern, build] of templates) {
        const match = value.match(pattern);
        if (match) {
            return normalizeAscii(build(...match));
        }
    }

    const composite = translateComposite(value, options);
    if (composite) {
        return composite;
    }

    const translated = translateTokenized(value);
    const normalizedTranslated = normalizeAscii(translated);
    if (!normalizedTranslated || normalizeKey(normalizedTranslated) === normalizeKey(value)) {
        return normalizeAscii(fallbackText(value, options));
    }

    const tag = String(options.tagName || '');
    if (tag === 'DisplayName') {
        return titleCaseAscii(normalizedTranslated);
    }

    return normalizedTranslated;
}

