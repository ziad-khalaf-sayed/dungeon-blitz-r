function normalizeKey(value: string): string {
    return String(value ?? '').trim().replace(/\s+/g, ' ');
}

function stableId(value: string): string {
    return crypto.createHash('sha1').update(value).digest('hex').slice(0, 6).toUpperCase();
}

function translateWord(word: string): string {
    const asciiWord = word.replace(/[’‘]/g, "'");
    const exact = EXACT_PHRASES[asciiWord] ?? PROPER_PHRASES[asciiWord];
    if (exact) {
        return exact;
    }

    const normalized = asciiWord.toLowerCase().replace(/^'+|'+$/g, '');
    const possessive = normalized.replace(/'s$/i, '');
    if (Object.prototype.hasOwnProperty.call(WORDS, normalized)) {
        const translated = WORDS[normalized];
        return /^[A-Z]/.test(asciiWord) ? translated.charAt(0).toUpperCase() + translated.slice(1) : translated;
    }
    if (/^[A-Za-z][A-Za-z.-]*'s$/i.test(asciiWord)) {
        const base = asciiWord.replace(/'s$/i, '');
        const proper = PROPER_PHRASES[base];
        if (proper) {
            return `de ${proper}`;
        }
        if (Object.prototype.hasOwnProperty.call(WORDS, possessive)) {
            return `de ${WORDS[possessive]}`;
        }
        if (/^[A-Z]/.test(base)) {
            return `de ${base}`;
        }
    }
    if (Object.prototype.hasOwnProperty.call(WORDS, possessive)) {
        const translated = WORDS[possessive];
        return /^[A-Z]/.test(asciiWord) ? translated.charAt(0).toUpperCase() + translated.slice(1) : translated;
    }
    return asciiWord;
}

function translateTokenized(value: string): string {
    return normalizeKey(
        value
            .replace(/[A-Za-z][A-Za-z'’]*/g, (word) => translateWord(word))
            .replace(/\s+([,.;:!?])/g, '$1')
    );
}

function fallbackText(source: string): string {
    if (/\b(help|save|protect)\b/i.test(source)) {
        return 'Preciso de ajuda!';
    }
    if (/\b(warning|beware)\b/i.test(source)) {
        return 'Cuidado!';
    }
    if (/\b(burn|fire|ashes|ash)\b/i.test(source)) {
        return 'Tudo vai queimar!';
    }
    if (/\b(die|kill|slay|destroy|annihilation|curse|blood)\b/i.test(source)) {
        return 'Voce vai cair aqui!';
    }
    if (/\b(come|rise|charge|attack|swarm|defend|guard|to me)\b/i.test(source)) {
        return 'Ataquem agora!';
    }
    if (/\b(human|trespasser|thief|thieves|usurper|intruder)\b/i.test(source)) {
        return 'Intruso!';
    }
    return `Fala localizada ${stableId(source)}.`;
}

export function localizeUnknownPortugueseText(source: string): string {
    const value = normalizeKey(source);
    if (!value || !/[A-Za-z]{2,}/.test(value)) {
        return source;
    }

    const mustBeLevel = value.match(/^Must be level\s+(.+?)\s+to upgrade$/i);
    if (mustBeLevel) {
        return normalizeDialogueTextForClient(`É necessário nível ${mustBeLevel[1]} para melhorar`, 'pt-br');
    }

    const charmRecipes = value.match(/^Unlocks Rank\s+(.+?)\s+charm recipes$/i);
    if (charmRecipes) {
        return normalizeDialogueTextForClient(`Libera receitas de gemas de Ranque ${charmRecipes[1]}`, 'pt-br');
    }

    const exact = EXACT_PHRASES[value] ?? PROPER_PHRASES[value];
    if (exact) {
        return normalizeDialogueTextForClient(exact, 'pt-br');
    }

    const translated = translateTokenized(value);
    if (translated && translated.toLowerCase() !== value.toLowerCase()) {
        const fixed = translated.replace(/\bMagia Forja\b/g, 'Forja Mágica');
        return normalizeDialogueTextForClient(fixed, 'pt-br');
    }

    return normalizeDialogueTextForClient(fallbackText(value), 'pt-br');
}
